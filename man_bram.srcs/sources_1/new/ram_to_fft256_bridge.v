// Module: RAM to FFT256 Bridge
// Description: Reads 256 complex samples from RAM64, feeds to combinational 
//              FFT-256, captures 256 complex outputs, writes to RAM128.
//////////////////////////////////////////////////////////////////////////////

`timescale 1ns / 1ps

module ram_to_fft256_bridge #(
    parameter DEPTH = 256
) (
    input  wire         i_clk,
    input  wire         i_rst_n,
    input  wire         i_start,

    // RAM64 read interface
    output reg          o_ram_rd_en,
    output reg  [7:0]   o_ram_rd_addr,
    input  wire         i_ram_rd_dv,
    input  wire [127:0] i_ram_rd_data,

    // RAM128 write interface
    output reg          o_ram_wr_en,
    output reg  [7:0]   o_ram_wr_addr,
    output reg  [127:0] o_ram_wr_data,

    // Status outputs
    output reg          o_done
);

    // -----------------------------------------------------------------------
    // Internal storage for 256 complex samples (64-bit real + 64-bit imag)
    // -----------------------------------------------------------------------
    reg [63:0] fft_inr [0:255];  // Real inputs
    reg [63:0] fft_ini [0:255];  // Imaginary inputs
    
    // FFT outputs (combinational from fft_256 module)
    wire [63:0] fft_outr [0:255];
    wire [63:0] fft_outi [0:255];

    // -----------------------------------------------------------------------
    // FSM States
    // -----------------------------------------------------------------------
    localparam [1:0] ST_IDLE  = 2'd0;
    localparam [1:0] ST_LOAD  = 2'd1;
    localparam [1:0] ST_WRITE = 2'd2;

    reg [1:0] state;
    reg [8:0] load_addr;
    reg [8:0] write_addr;

    integer k;

    // -----------------------------------------------------------------------
    // Main FSM
    // -----------------------------------------------------------------------
    always @(posedge i_clk) begin
        if (!i_rst_n) begin
            o_ram_rd_en <= 1'b0;
            o_ram_rd_addr <= 8'd0;
            o_ram_wr_en <= 1'b0;
            o_ram_wr_addr <= 8'd0;
            o_ram_wr_data <= 128'd0;
            o_done <= 1'b0;
            state <= ST_IDLE;
            load_addr <= 9'd0;
            write_addr <= 9'd0;
            for (k = 0; k < 256; k = k + 1) begin
                fft_inr[k] <= 64'd0;
                fft_ini[k] <= 64'd0;
            end
        end else begin
            o_ram_rd_en <= 1'b0;
            o_ram_wr_en <= 1'b0;
            o_done <= 1'b0;

            case (state)
                ST_IDLE: begin
                    if (i_start) begin
                        load_addr <= 9'd0;
                        state <= ST_LOAD;
                    end
                end

                ST_LOAD: begin
                    if (load_addr < 9'd256) begin
                        o_ram_rd_en <= 1'b1;
                        o_ram_rd_addr <= load_addr[7:0];
                        
                        if (i_ram_rd_dv) begin
                            // Extract real (bits 63:0) and imag (bits 127:64)
                            fft_inr[load_addr[7:0]] <= i_ram_rd_data[63:0];
                            fft_ini[load_addr[7:0]] <= i_ram_rd_data[127:64];
                            load_addr <= load_addr + 9'd1;
                        end
                    end else begin
                        load_addr <= 9'd0;
                        write_addr <= 9'd0;
                        state <= ST_WRITE;
                    end
                end

                ST_WRITE: begin
                    if (write_addr < 9'd256) begin
                        o_ram_wr_en <= 1'b1;
                        o_ram_wr_addr <= write_addr[7:0];
                        // Pack: bits 127:64 = imag, bits 63:0 = real
                        o_ram_wr_data <= {fft_outi[write_addr[7:0]], fft_outr[write_addr[7:0]]};
                        write_addr <= write_addr + 9'd1;
                    end else begin
                        o_done <= 1'b1;
                        state <= ST_IDLE;
                    end
                end
            endcase
        end
    end

